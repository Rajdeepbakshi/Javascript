//abs
// abs(positive_value) --> positive_value
// abs(negative_value) --> positive_counterpart_of that negative value
// |x| --> Ex: |10| -> 10, |-8| -> 8


console.log(Math.abs(-8));

//Round off

console.log(Math.round(10.4));

//floor -> it returns first integer just less than or equal to x
console.log(Math.floor(10.4));

//ceil -> it returns first integer just greater than or equal to x
console.log(Math.ceil(10.4));

//Min and max
console.log(Math.min(10, 20, 30, 40, 50)); //10
console.log(Math.max(10, 20, 30, 40, 50)); //50

//square root of a number
console.log(Math.sqrt(25)); //5

